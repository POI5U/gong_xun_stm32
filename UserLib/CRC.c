uint8_t CRC8(uint8_t *p, uint8_t len)
{
    uint8_t i = 0, crc8 = p[0];
    for(i = 1; i < len; i++)
    {
        crc8 = crc8Table[crc8 ^ (*(p + i))];
    }
    return crc8;
}

uint16_t CRC16(uint8_t *puchMsg,uint8_t usDataLen)
{
	uint8_t uchCRCHi=0xFF;
	uint8_t uchCRCLo=0xFF;
	uint8_t uIndex;
	while(usDataLen--){
		uIndex = uchCRCHi ^ (*puchMsg++) ; 
		uchCRCHi = uchCRCLo ^ auchCRCHi[uIndex] ;
		uchCRCLo = auchCRCLo[uIndex] ;
	}
	return ((uint16_t)uchCRCHi << 8 | uchCRCLo) ;
}



void CRC16_ModbusAdd(uint8_t *puchMsg,uint8_t ALL_Long)
{
	uint16_t crc = CRC16(puchMsg, ALL_Long - 2);
	puchMsg[ALL_Long - 2] = crc >> 8;
	puchMsg[ALL_Long - 1] = crc & 0xFF;
}


int8_t CRC16_modbuscheck(uint8_t *puchMsg, uint8_t ALL_Long)
{
	uint16_t crc = CRC16(puchMsg, ALL_Long - 2);
	if( puchMsg[ALL_Long - 2] != (crc >> 8 ) ||	
		puchMsg[ALL_Long - 1] != (crc & 0xFF)){
		return -1;
	}
	return 0;
}

